            // Upper Camel Case, las clases siempre van con la primera en mayuscula
public class NamingJava {
    public static void main(String[] args) {
        int celphone = 33337777;
        int celPhone = 55558888;
        System.out.println(celphone);
        System.out.println(celPhone);

        String $countryName = "Argentina";
        String _backgroundColor = "Red";

        String Currency$ = "MXN";
        String background_color = "Blue";

        int POSITION = -5;
        int MAX_WIDTH = 9999;
        int MIN_WIDTH = 1;

        // Lower Camel Case, se usa para nombres de variables y metodos
        String fullName = "Daniel Alberto Vinzia";
        int sizeInCentimeters = 32;
    }
}
